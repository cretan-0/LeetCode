class Solution(object):
    def numSubarraysWithSum(self, nums, goal):
        cnt = 0

        for i in range(len(nums)):
            sub_array = []
            sub_array.append(nums[i])
            res = 0
            for j in range(i+1, len(nums)):
                sub_array.append(nums[j])
            for num in sub_array:
                res += num
                if res == goal:
                   cnt += 1
        return cnt


